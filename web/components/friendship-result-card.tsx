"use client";

import { useEffect, useRef, useState } from "react";
import type { Lang } from "@/lib/i18n";

export interface FriendshipPerson { name: string; nakshatra: string; rasi: string }
export interface FriendshipBand { key: string; ta: string; en: string }
export interface FriendshipSection { key: string; title: { ta: string; en: string }; text: { ta: string; en: string } }
export interface FriendshipReport {
  person1: FriendshipPerson;
  person2: FriendshipPerson;
  band: FriendshipBand;
  scoreRange: string;
  headline: { ta: string; en: string };
  sections: FriendshipSection[];
}

const BAND_THEME: Record<string, { a: string; b: string; accent: string; ink: string }> = {
  NATURAL_COMPANIONS: { a: "#0F3D24", b: "#1E5631", accent: "#7CEFA0", ink: "#EAFBF0" },
  COMPLEMENTARY_PAIR: { a: "#13294B", b: "#1E3A5F", accent: "#8BBDF7", ink: "#E7F0FE" },
  GROWTH_FRIENDS:     { a: "#5A360E", b: "#7A4A12", accent: "#FBD24E", ink: "#FFF4D6" },
  COSMIC_TEACHERS:    { a: "#2A1450", b: "#3B1E5F", accent: "#C9A8FF", ink: "#F1E9FF" },
};

function roundRect(ctx: CanvasRenderingContext2D, x: number, y: number, w: number, h: number, r: number) {
  ctx.beginPath();
  ctx.moveTo(x + r, y);
  ctx.arcTo(x + w, y, x + w, y + h, r);
  ctx.arcTo(x + w, y + h, x, y + h, r);
  ctx.arcTo(x, y + h, x, y, r);
  ctx.arcTo(x, y, x + w, y, r);
  ctx.closePath();
}

function wrap(ctx: CanvasRenderingContext2D, text: string, x: number, y: number, maxW: number, lh: number, maxLines = 3) {
  const words = text.split(" ");
  let line = "";
  let lines = 0;
  for (const w of words) {
    const test = line ? `${line} ${w}` : w;
    if (ctx.measureText(test).width > maxW && line) {
      ctx.fillText(line, x, y); line = w; y += lh; lines++;
      if (lines >= maxLines) { ctx.fillText("…", x, y); return; }
    } else line = test;
  }
  if (line) ctx.fillText(line, x, y);
}

const FONT = "'Noto Sans Tamil', 'Latha', 'Nirmala UI', system-ui, sans-serif";

export function FriendshipResultCard({ report, lang }: { report: FriendshipReport; lang: Lang }) {
  const canvasRef = useRef<HTMLCanvasElement>(null);
  const [preview, setPreview] = useState<string | null>(null);
  const theme = BAND_THEME[report.band.key] ?? BAND_THEME.GROWTH_FRIENDS;

  useEffect(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const W = 1080, H = 1080;
    canvas.width = W; canvas.height = H;
    const ctx = canvas.getContext("2d");
    if (!ctx) return;

    const bg = ctx.createLinearGradient(0, 0, W, H);
    bg.addColorStop(0, theme.a); bg.addColorStop(1, theme.b);
    ctx.fillStyle = bg; ctx.fillRect(0, 0, W, H);
    const glow = ctx.createRadialGradient(W / 2, 420, 40, W / 2, 420, 560);
    glow.addColorStop(0, theme.accent + "26"); glow.addColorStop(1, "transparent");
    ctx.fillStyle = glow; ctx.fillRect(0, 0, W, H);

    ctx.textAlign = "center";
    ctx.fillStyle = theme.accent;
    ctx.font = `bold 30px ${FONT}`;
    ctx.fillText(lang === "ta" ? "நட்பு பொருத்தம்" : "FRIENDSHIP STYLE", W / 2, 130);

    // Names
    ctx.fillStyle = theme.ink;
    ctx.font = `bold 64px ${FONT}`;
    ctx.fillText(`${report.person1.name}  &  ${report.person2.name}`, W / 2, 230);

    // Two connected stars motif
    ctx.fillStyle = theme.accent;
    ctx.font = `48px ${FONT}`;
    ctx.fillText("✦ ─────── ✦", W / 2, 300);

    // Nakshatra / rasi line
    ctx.fillStyle = theme.ink;
    ctx.font = `30px ${FONT}`;
    ctx.fillText(`${report.person1.nakshatra} · ${report.person1.rasi}`, W / 2, 360);
    ctx.fillText(`${report.person2.nakshatra} · ${report.person2.rasi}`, W / 2, 404);

    // Band label
    ctx.fillStyle = theme.accent;
    ctx.font = `bold 86px ${FONT}`;
    wrap(ctx, lang === "ta" ? report.band.ta : report.band.en, W / 2, 560, W - 160, 96, 2);

    // Headline / one-line description
    ctx.fillStyle = theme.ink;
    ctx.font = `34px ${FONT}`;
    wrap(ctx, lang === "ta" ? report.headline.ta : report.headline.en, W / 2, 720, W - 200, 48, 4);

    // Watermark footer
    ctx.fillStyle = theme.accent;
    roundRect(ctx, 290, H - 130, W - 580, 60, 30); ctx.fill();
    ctx.fillStyle = theme.a;
    ctx.font = `bold 26px ${FONT}`;
    ctx.fillText("Generated by Vinaadi AI · vinaadi.ai", W / 2, H - 90);

    setPreview(canvas.toDataURL("image/png"));
  }, [report, lang, theme]);

  async function share() {
    if (!preview) return;
    try {
      const blob = await (await fetch(preview)).blob();
      const file = new File([blob], "vinaadi-friendship.png", { type: "image/png" });
      if (typeof navigator !== "undefined" && navigator.share && navigator.canShare?.({ files: [file] })) {
        await navigator.share({ files: [file], title: "Vinaadi AI · Friendship" });
        return;
      }
    } catch { /* fall through */ }
    const a = document.createElement("a");
    a.href = preview; a.download = "vinaadi-friendship.png"; a.click();
  }

  return (
    <div style={{ display: "flex", flexDirection: "column", alignItems: "center", gap: "14px" }}>
      <canvas ref={canvasRef} style={{ display: "none" }} />
      {preview && (
        <img src={preview} alt="friendship card" style={{ width: "min(100%, 360px)", borderRadius: "16px", boxShadow: "0 10px 30px rgba(0,0,0,0.25)" }} />
      )}
      <button
        type="button"
        onClick={() => void share()}
        style={{ padding: "11px 26px", borderRadius: "999px", border: "none", background: theme.accent, color: theme.a, fontWeight: 800, cursor: "pointer", fontSize: "0.95rem" }}
      >
        {lang === "ta" ? "பகிர் / பதிவிறக்கம்" : "Share / Download"}
      </button>
    </div>
  );
}
